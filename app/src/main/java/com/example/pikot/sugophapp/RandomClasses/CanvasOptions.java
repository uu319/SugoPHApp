public class CanvasOptions {
    String optionName;
    String optionId;

    public CanvasOptions(String optionName, String optionId) {
        this.optionName = optionName;
        this.optionId = optionId;
    }

    public String getOptionName() {
        return optionName;
    }

    public String getOptionId() {
        return optionId;
    }
}
